  let cart=JSON.parse(localStorage.getItem("cart")) || [];
  function Display(data){
    container.innerHTML=null;
    data.forEach(function (el) {
    let div = document.createElement("div");
    let ratdiv = document.createElement("div");
    let image_url = document.createElement("img");
    image_url.setAttribute("src", el.image);
    image_url.addEventListener("click",function(){
      show(el)
    })

    let btn = document.createElement("button");
    btn.innerText = "ADD TO BAG";
    btn.addEventListener("click",function(){
      addToCart(el);

    })
    let proName = document.createElement("h2");
    proName.innerText = el.name;
    let dataPrice = document.createElement("p");
    dataPrice.innerText =el.title;
    let price=document.createElement("h1")
    price.innerText="$"+el.price;
    let t=document.createElement("h6")
    t.innerText="Best Seller";
    let star=document.createElement("img")
    star.src=el.star;
    let rat=document.createElement("h5")
    rat.innerText=el.rat;
    ratdiv.append(star,rat)

    div.append(image_url,t, proName, dataPrice,price,ratdiv,btn);

    container.append(div);
  });
}
  function addToCart(el){
    let flag=false;
    for(let i=0;i<cart.length;i++){
      if(cart[i].name===el.name){
        alert("product already in cart");
        flag=true;
        return;
      }
    }
    if(flag==false){
    cart.push(el);
    alert("product added to cart");
    localStorage.setItem("cart",JSON.stringify(cart));
    }
  }
